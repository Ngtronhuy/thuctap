void main() {
  // vòng lặp for
  print(" vòng lặp for:");
  for (int i = 1; i <= 5; i++) {
    print(i);
  }

  //  vòng lặp while
  print(" vòng lặp while:");
  int j = 1;
  while (j <= 5) {
    print(j);
    j++;
  }

  // vòng lặp do-while
  print(" vòng lặp do-while:");
  int k = 1;
  do {
    print(k);
    k++;
  } while (k <= 5);
}